function displayBabyNames(divid, teamName) {
    const studentsList = document.getElementById(divid);

    // Define Hindi alphabets (A, B, C... in Hindi)
    const hindiAlphabets = [
        'अ', 'आ', 'इ', 'ई', 'उ', 'ऊ', 'ऋ', 'ए', 'ऐ', 'ओ', 'औ', 'अं', 'अः',
        'क', 'ख', 'ग', 'घ', 'च', 'छ', 'ज', 'झ', 'ञ', 'ट', 'ठ', 'ड', 'ढ', 'ण',
        'त', 'थ', 'द', 'ध', 'न', 'प', 'फ', 'ब', 'भ', 'म', 'य', 'र', 'ल', 'व',
        'श', 'ष', 'स', 'ह', 'क्ष', 'त्र', 'ज्ञ'
    ];

    // Create alphabet filter buttons
    let alphabetFilterHTML = '';
    hindiAlphabets.forEach(letter => {
        alphabetFilterHTML += `
            <button class="alphabet-btn" data-letter="${letter}" onclick="filterByLetter('${letter}')">${letter}</button>
        `;
    });

    // Add alphabet filter to the page
    document.querySelector('.alphabet-filter').innerHTML = alphabetFilterHTML;

    // Start with empty content, later filled dynamically
    let contentHTML = '';

    // Loop through each alphabet letter (A, B, C... in Hindi)
    hindiAlphabets.forEach(letter => {
        const letterGroup = teamName[letter]; // Get all names under a specific letter

        contentHTML += `
            <div class="letter-section" id="section-${letter}" style="display:block;">
                <h2 class="letter-title"><span class="letter-letter">${letter}</span></h2>
                <div class="row" id="letter-${letter}-content">
        `;

        if (letterGroup && letterGroup.length > 0 && !(letterGroup.length === 1 && letterGroup[0].babyName === "There are no names available.")) {
            letterGroup.forEach(baby => {
                contentHTML += generateBabyNameHTML(baby);
            });
        } else {
            contentHTML += `
                <div class="col-12">
                    <p>Currently, there are no names available for this letter.</p>
                </div>
            `;
        }

        contentHTML += `</div></div>`;
    });

    studentsList.innerHTML = contentHTML;

    // Initialize the gender filter with 'all' selected by default
    window.selectedGender = 'all';
    window.selectedLetter = null;
}

function generateBabyNameHTML(baby) {
    let babyHTML = '';

    if (baby.babyG === "g") {
        babyHTML = `
            <div class="col-12 col-sm-12 col-md-6">
                <div class="babyName-entry babyName_girl bg-white">
                    <div class="babyname-content">
                        <div class="image girl" style="background-image: url('assets/img/icon_girl.png');"></div>
                        <div class="text">
                            <h3 class="babytitle">${baby.babyName}</h3>
                            <div><span>Meaning: ${baby.meaningH}</span></div>
                        </div>
                    </div>
                </div>
            </div>
        `;
    } else {
        babyHTML = `
            <div class="col-12 col-sm-12 col-md-6">
                <div class="babyName-entry babyName_boy bg-white">
                    <div class="babyname-content">
                        <div class="image boy" style="background-image: url('assets/img/icon_boy.png');"></div>
                        <div class="text">
                            <h3 class="babytitle">${baby.babyName}</h3>
                            <div><span>Meaning: ${baby.meaningH}</span></div>
                        </div>
                    </div>
                </div>
            </div>
        `;
    }

    return babyHTML;
}

// Function to handle letter filter
function filterByLetter(letter) {
    // Update active class for alphabet buttons
    document.querySelectorAll('.alphabet-btn').forEach(button => {
        button.classList.remove('active'); // Remove active from all alphabet buttons
    });

    // Add active class to the clicked alphabet button
    const selectedButton = document.querySelector(`.alphabet-btn[data-letter="${letter}"]`);
    if (selectedButton) {
        selectedButton.classList.add('active');
    }

    // Update the selected letter filter state
    window.selectedLetter = letter;

    // Apply the filters (both gender and letter)
    applyFilters();
}

// Function to handle gender filter
function filterByGender(gender) {
    // Update active class for gender buttons
    document.querySelectorAll('.gender-btn').forEach(button => {
        button.classList.remove('active'); // Remove active from all gender buttons
    });

    // Add active class to the clicked button
    const selectedButton = document.querySelector(`.gender-btn[data-gender="${gender}"]`);
    if (selectedButton) {
        selectedButton.classList.add('active');
    }

    // Update the selected gender filter state
    window.selectedGender = gender;

    // Apply the filters (both gender and letter)
    applyFilters();
}

// Function to apply filters
function applyFilters() {
    const allSections = document.querySelectorAll('.letter-section');

    allSections.forEach(section => {
        const letter = section.id.replace('section-', '');
        const letterContent = document.getElementById(`letter-${letter}-content`);

        // Filter the names inside the section based on selected gender and letter
        const allBabyNames = letterContent.querySelectorAll('.babyName-entry');

        // Apply the gender and letter filters
        let hasVisibleNames = false; // Flag to check if there are visible names in this section

        allBabyNames.forEach(babyName => {
            const isGirl = babyName.classList.contains('babyName_girl');
            const isBoy = babyName.classList.contains('babyName_boy');

            // Check if both gender and letter match the selected filters
            if ((window.selectedGender === 'all' ||
                (window.selectedGender === 'g' && isGirl) ||
                (window.selectedGender === 'b' && isBoy)) &&
                (window.selectedLetter === null || letter === window.selectedLetter)) {
                babyName.style.display = 'block';
                hasVisibleNames = true;
            } else {
                babyName.style.display = 'none';
            }
        });

        // Toggle visibility of the section based on whether it has visible names
        section.style.display = hasVisibleNames ? 'block' : 'none';
    });
}

function clearFilters() {
    // Reset gender filter and update UI
    window.selectedGender = 'all';
    document.querySelectorAll('.gender-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    document.querySelector('.gender-btn[data-gender="all"]').classList.add('active'); // Default to "All"

    // Reset alphabet filter and update UI
    window.selectedLetter = null;
    document.querySelectorAll('.alphabet-btn').forEach(btn => {
        btn.classList.remove('active');
    });

    applyFilters(); // Re-apply filters to reset all entries
}

